#include "fdf.h"

unsigned int hash(int x, int y, unsigned int seed)
{
	unsigned int h;

	h = seed;
	h ^= x * 374761393U;
	h ^= y * 668265263U;
	h *= 3284157443U;
	h ^= h >> 13;
	h *= 1911520717U;
	h ^= h >> 16;
	return (h);
}
